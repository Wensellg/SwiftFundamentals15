/*:
 ## Exercise - Compound Assignment
*/

// Start at 10
var value = 10

// Add using compound assignment
value += 5
print(value)   // 15

// Multiply using compound assignment
value *= 2
print(value)   // 30

/*:
 Penny Bank
*/

var pennyBank = 0

// Neighbor gives you $10
pennyBank += 10
print(pennyBank)

// Earn $20 more
pennyBank += 20
print(pennyBank)

// Spend half your money
pennyBank /= 2
print(pennyBank)

// Triple what's left
pennyBank *= 3
print(pennyBank)

// Spend $3
pennyBank -= 3
print(pennyBank)
